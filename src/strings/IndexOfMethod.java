package strings;

public class IndexOfMethod {
   
	public static void main(String[] args) {
		String str="bangalore";
		System.out.println(str.indexOf("a",3));//4
		System.out.println(str.indexOf("a",0));//1
		System.out.println(str.indexOf("a",4));//4
		System.out.println(str.indexOf("a",5));//-1
		
		
		
		
		
	}

}
