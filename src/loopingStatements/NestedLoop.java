package loopingStatements;

public class NestedLoop {

	public static void main(String[] args) {

		for(int i=1;i<=5;i++) {

			for(int j=1;j<=4;j++) {
				System.out.println(j);
			}

		}

		System.out.println("Second Program");

		for(int k=1;k<=5;k++) {

			for(int l=1;l<=4;l++) {
				System.out.println(k);
			}

		}	



	}

}
